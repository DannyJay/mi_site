    //翻转
    var turn = function(target,time,opts){
        target.find('li').hover(function(){
            $(this).find('.former').stop().animate(opts[0],time,function(){
                $(this).hide().next().show();
                $(this).next().animate(opts[1],time);
            });
        },function(){
            $(this).find('.latter').animate(opts[0],time,function(){
                $(this).hide().prev().show();
                $(this).prev().animate(opts[1],time);
            });
        });
    }
    var verticalOpts = [{'width':0},{'width':'260px'}];
    turn($('.newslist'),100,verticalOpts);
